class Solution70 {
    public int climbStairs(int n) {
        if(n == 1){
            return 1;
        }
        if(n == 2){
            return 2;
        }
        int left = 1,right = 2;
        int times = n -2;
        int tmp;
        while (times > 0){
            tmp = right;
            right += left;
            left = tmp;
            times --;
        }
        return right;
    }
}
class Main70{
    public static void main(String[] args) {
        Solution70 solution70 = new Solution70();
        for (int i = 1; i <= 41; i++) {
            System.out.println(solution70.climbStairs(i));
        }
    }
}
